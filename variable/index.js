// Primitívek.
// Boolean. true|false
var bool = true;
console.log(typeof bool);

// Number. 1, 2, 3
var num = 2.3;
console.log(typeof num);

// String. "Hello Fornetti!"
var greet = "Hello Fornetti!";
console.log(typeof greet);

// Deklaráció. Alapértelmezetten undefined.
var name;
console.log(typeof name);
name = "Józsi"; // felül definiálom a változót
console.log(typeof name);

// Null. Ezzel jelezzük, hogy nem vesz fel értéket a változó.
var nullable = null;
console.log(typeof nullable);